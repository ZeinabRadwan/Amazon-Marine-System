let currentLang = 'ar';

function setLanguage(lang) {
    currentLang = lang;
    const t = translations[lang];
    const isAR = lang === 'ar';

    // HTML direction & lang
    document.documentElement.lang = isAR ? 'ar' : 'en';
    document.documentElement.dir = isAR ? 'rtl' : 'ltr';
    document.title = t.pageTitle;

    // Update dropdown items
    document.querySelectorAll('.lang-dropdown-item').forEach(item => {
        const isActive = item.dataset.lang === lang;
        item.classList.toggle('active', isActive);
        // Show/hide checkmark
        let check = item.querySelector('i.bx-check');
        if (isActive && !check) {
            const icon = document.createElement('i');
            icon.className = 'bx bx-check';
            item.appendChild(icon);
        } else if (!isActive && check) {
            check.remove();
        }
    });

    // Update badge
    const badge = document.getElementById('langCodeBadge');
    if (badge) badge.textContent = lang.toUpperCase();

    // Close dropdown
    const dropdown = document.getElementById('langDropdown');
    if (dropdown) dropdown.classList.remove('show');

    // Apply all translations
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.dataset.i18n;
        if (t[key] !== undefined) {
            if (el.dataset.i18nAttr === 'placeholder') {
                el.placeholder = t[key];
            } else if (el.dataset.i18nHtml) {
                el.innerHTML = t[key];
            } else {
                el.textContent = t[key];
            }
        }
    });
}

function toggleLangDropdown(e) {
    e.stopPropagation();
    const dropdown = document.getElementById('langDropdown');
    dropdown.classList.toggle('show');
}

// Close dropdown when clicking outside
document.addEventListener('click', (e) => {
    const dropdown = document.getElementById('langDropdown');
    if (dropdown && !e.target.closest('.lang-dropdown')) {
        dropdown.classList.remove('show');
    }
});

document.addEventListener('DOMContentLoaded', () => {
    setLanguage('ar');
});
